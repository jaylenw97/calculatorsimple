def main():
    #Add
    num_a = int(input("Enter a number to add "))
    num_b = int(input("Enter another number to add: "))
    print("{}".format(num_a + num_b))

    #Subtract
    num_a = int(input("Enter a number to subtract from: "))	
    num_b = int(input("Enter a second number to subtract from: "))
    print("{}".format(num_a - num_b))
   
    #Multiply
    num_a = int(input("Enter a number to multiply from: "))
    num_b = int(input("Enter a second number to multiply from: "))
    print("{}".format(num_a * num_b))

main()
